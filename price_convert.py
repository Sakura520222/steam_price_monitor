def to_cny(price, currency):
    """
    将给定价格转换为人民币价格
    
    Args:
        price (float): 原始价格
        currency (str): 货币代码
        
    Returns:
        float: 人民币价格，如果无法转换则返回None
    """
    if price is None or currency is None:
        return None
        
    # 汇率定义（以人民币为基准） - 更新日期：2025年12月8日
    exchange_rates = {
    'CNY': 1.0,        # 人民币
    'USD': 7.0764,     # 美元 (2025-12-08 中国外汇交易中心)
    'EUR': 8.2374,     # 欧元 (2025-12-08 中国外汇交易中心)
    'GBP': 9.42,       # 英镑 (2025-12-08 中国外汇交易中心)
    'JPY': 0.045563,   # 日元 (基于100日元=4.5563人民币换算) (2025-12-08 中国外汇交易中心)
    'KRW': 0.004845,   # 韩元 (基于100人民币=20641韩元换算)
    'HKD': 0.90919,    # 港币 (2025-12-08 中国外汇交易中心)
    'TWD': 0.23,       # 新台币 (搜索结果未提供，保留原值)
    'SGD': 5.4359,     # 新加坡元
    'CAD': 5.0575,     # 加拿大元
    'AUD': 4.6080,     # 澳大利亚元
    'CHF': 8.8168,     # 瑞士法郎
    'RUB': 0.088283,   # 俄罗斯卢布 (基于100人民币=1132.41卢布换算)
    'UAH': 0.1718,     # 乌克兰格里夫纳 (搜索结果未提供，保留原值)
    'BRL': 1.4,        # 巴西雷亚尔 (搜索结果未提供，保留原值)
    'INR': 0.087,      # 印度卢比 (搜索结果未提供，保留原值)
    'MXN': 0.3869,     # 墨西哥比索 (基于100人民币=258.44比索换算)
    'IDR': 0.00048     # 印尼盾 (搜索结果未提供，保留原值)
    }
    
    # 转换为大写以匹配键
    currency = currency.upper()
    
    # 如果货币不在汇率表中，返回None
    if currency not in exchange_rates:
        return None
    
    # 计算并返回人民币价格
    return round(price * exchange_rates[currency], 2)
